/// The extended colors map and functions to parse and return extended color names.

import 'dart:ui';

/// Parses [source] as an extended color name and returns the [Color] value, or null if the [source]
/// string does not contain a valid color name.
@override
Color? parseColor(String source) {
  return colorMap[source];
}

/// Returns the name of the given [color] from the extended color map, or null if the [color] is not
/// an extended color.
@override
String? getColorName(Color color) {
  return _reversedColorMap[color];
}

